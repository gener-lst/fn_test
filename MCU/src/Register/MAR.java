package Register;

public class MAR extends Register {
    public MAR() {
        super();
    }
    private int pageNum;
    private int offSet;
    public int getPageNum() {
        return pageNum;
    }

    public void setPageNum(int pageNum) {
        this.pageNum = pageNum;
    }

    public int getOffSet() {
        return offSet;
    }

    public void setOffSet(int offSet) {
        this.offSet = offSet;
    }

}
